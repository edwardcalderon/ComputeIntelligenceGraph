#!/usr/bin/env node
/**
 * Compile JSON catalogs into TypeScript modules for type-safe imports.
 *
 * Reads catalogs/{namespace}/{locale}.json files and generates:
 *   src/locales/{namespace}.ts — typed catalog with all locales
 *   src/locales/index.ts       — barrel export
 *   src/locales/keys.ts        — union type of all translation keys
 *
 * Run as part of the build step: `node scripts/compile-catalogs.mjs && tsc`
 */

import { readFileSync, writeFileSync, readdirSync, existsSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const catalogsDir = join(__dirname, "..", "catalogs");
const outDir = join(__dirname, "..", "src", "locales");

// Ensure output directory
mkdirSync(outDir, { recursive: true });

const LOCALES = ["en", "es", "zh"];

const namespaces = readdirSync(catalogsDir, { withFileTypes: true })
  .filter((d) => d.isDirectory())
  .map((d) => d.name)
  .sort();

const allKeys = new Set();
const barrelExports = [];

for (const ns of namespaces) {
  const catalogs = {};

  for (const locale of LOCALES) {
    const filePath = join(catalogsDir, ns, `${locale}.json`);
    if (!existsSync(filePath)) {
      console.warn(`Warning: missing catalog ${ns}/${locale}.json`);
      catalogs[locale] = {};
      continue;
    }
    catalogs[locale] = JSON.parse(readFileSync(filePath, "utf-8"));
  }

  // Collect all keys from source locale
  for (const key of Object.keys(catalogs.en || {})) {
    allKeys.add(key);
  }

  // Generate TypeScript module
  const lines = [
    `// Auto-generated by scripts/compile-catalogs.mjs — DO NOT EDIT`,
    `import type { TranslationCatalog, SupportedLocale } from "../types.js";`,
    ``,
  ];

  for (const locale of LOCALES) {
    const json = JSON.stringify(catalogs[locale], null, 2);
    lines.push(`const ${locale}: TranslationCatalog = ${json};`);
    lines.push(``);
  }

  lines.push(`const catalogs: Record<SupportedLocale, TranslationCatalog> = { ${LOCALES.join(", ")} };`);
  lines.push(``);
  lines.push(`export default catalogs;`);
  lines.push(`export { ${LOCALES.join(", ")} };`);
  lines.push(``);

  writeFileSync(join(outDir, `${ns}.ts`), lines.join("\n"));
  barrelExports.push(
    `export { default as ${ns} } from "./${ns}.js";`,
  );
}

// Generate keys type
const keysLines = [
  `// Auto-generated by scripts/compile-catalogs.mjs — DO NOT EDIT`,
  ``,
  `/** Union of all known translation keys across all namespaces. */`,
  `export type TranslationKey =`,
];

const sortedKeys = [...allKeys].sort();
for (let i = 0; i < sortedKeys.length; i++) {
  const sep = i < sortedKeys.length - 1 ? "" : ";";
  keysLines.push(`  | "${sortedKeys[i]}"${sep}`);
}
keysLines.push(``);

writeFileSync(join(outDir, "keys.ts"), keysLines.join("\n"));

// Generate barrel
const barrel = [
  `// Auto-generated by scripts/compile-catalogs.mjs — DO NOT EDIT`,
  ...barrelExports,
  `export type { TranslationKey } from "./keys.js";`,
  ``,
];

writeFileSync(join(outDir, "index.ts"), barrel.join("\n"));

console.log(
  `Compiled ${namespaces.length} namespace(s), ${sortedKeys.length} unique key(s), ${LOCALES.length} locale(s).`,
);
